# Loops in Python 1.for, 2.while

# for loop example

# Defining a list
days_of_week = ['mon', 'tue', 'wed', 'thu', 'fri', 'sat', 'sun']

# Printing contents of the list
print("Days of the week")
for days in days_of_week:
	print(days) 

	
# while loop example	

# Defining a variable by name x
x = 0

# printing 0 to 10 
while x <= 10:
	print("Current value of x: ",x)
	x += 1 # reassigning x to x by adding 1 to it